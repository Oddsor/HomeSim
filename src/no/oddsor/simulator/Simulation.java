            step = new SimulationStep(startPos);
            
            ActionListener timeListener = new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent ae) {
                    painter.setPosition(step.nextStep());
                }
            };
            
            Timer tim = new Timer(100, timeListener);
            tim.start();
